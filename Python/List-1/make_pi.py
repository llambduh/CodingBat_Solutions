def make_pi():
  """
  Returns an array containing the first 3 digits of pi.
  
  Pi (π) is the mathematical constant approximately equal to 3.14159...
  This function returns the first three digits: 3, 1, and 4
  
  Args:
    None
    
  Returns:
    A list of integers [3, 1, 4] representing the first three digits of pi
  """
  
  # Return a hardcoded list containing the first three digits of pi
  # Pi ≈ 3.14159..., so the first three digits are 3, 1, and 4
  # Note: These are individual digits, not the decimal value 3.14
  # The list is created and returned directly without storing in a variable
  return [3, 1, 4]
  